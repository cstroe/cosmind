-- Project

CREATE SEQUENCE project_id_seq AS BIGINT START WITH 1 INCREMENT BY 1 MINVALUE 1 NO MAXVALUE;

CREATE TABLE project (
    id       BIGINT GENERATED BY DEFAULT AS SEQUENCE project_id_seq PRIMARY KEY,
    codename VARCHAR(10),
    summary  VARCHAR(256),
    url      VARCHAR(1024)
);


-- Story

CREATE SEQUENCE story_id_seq AS BIGINT START WITH 1 INCREMENT BY 1 MINVALUE 1 NO MAXVALUE;

CREATE TABLE story (
    id          BIGINT GENERATED BY DEFAULT AS SEQUENCE story_id_seq PRIMARY KEY,
    summary     VARCHAR(256),
    description LONGVARCHAR
);